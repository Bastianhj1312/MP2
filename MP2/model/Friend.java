package MP2.model;

public class Friend
{
   private String name;
    private int phone;
    private String address;
    private int postalcode;
    private String city;
    
    public Friend(String name, int phone, String address, int postalcode, String city) {
        this.name = name;
        this.phone = phone;
        this.address = address;
        this.postalcode = postalcode;
        this.city = city;
    }
    
}
